minN = 10**10
for n in range(2, 1000):
    n2 = bin(n)[2:] #первое действие алгоритма
    dl = len(n2)
    if dl % 2 != 0: #второе действие алгоритма
        n2 = n2[:dl // 2] + n2[dl // 2 + 1:]
    dl = len(n2) #обновление длины числа
    n2 = n2[dl // 2:] + n2[:dl // 2] #третье действие алгоритма
    r = int(n2, 2) #четвёртое действие алгоритма
    if r == 52:
        minN = min(minN, n)
print(minN)
